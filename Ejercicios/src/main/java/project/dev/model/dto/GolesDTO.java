package project.dev.model.dto;

public class GolesDTO {
	private int jug_rut;
	private	String jug_nombre;
	private int equ_cod;
	private String equ_nombre;
	private int gol_cantidad;
	
	public int getJug_rut() {
		return jug_rut;
	}
	public void setJug_rut(int jug_rut) {
		this.jug_rut = jug_rut;
	}
	public String getJug_nombre() {
		return jug_nombre;
	}
	public void setJug_nombre(String jug_nombre) {
		this.jug_nombre = jug_nombre;
	}
	public int getEqu_cod() {
		return equ_cod;
	}
	public void setEqu_cod(int equ_cod) {
		this.equ_cod = equ_cod;
	}
	public String getEqu_nombre() {
		return equ_nombre;
	}
	public void setEqu_nombre(String equ_nombre) {
		this.equ_nombre = equ_nombre;
	}
	public int getGol_cantidad() {
		return gol_cantidad;
	}
	public void setGol_cantidad(int gol_cantidad) {
		this.gol_cantidad = gol_cantidad;
	}
	

}
